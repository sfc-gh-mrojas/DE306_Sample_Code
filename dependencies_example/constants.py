# Define constants using uppercase variable names
PI = 3.14159
GRAVITY = 9.8
DEFAULT_NAME = "John Doe"

# Constants can also be defined within a class
class Colors:
    RED = "red"
    GREEN = "green"
    BLUE = "blue"

# Accessing constants
print("PI:", PI)
print("GRAVITY:", GRAVITY)
print("DEFAULT_NAME:", DEFAULT_NAME)

# Accessing constants within a class
print("Colors.RED:", Colors.RED)
print("Colors.GREEN:", Colors.GREEN)
print("Colors.BLUE:", Colors.BLUE)
